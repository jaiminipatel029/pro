package program4;

public class EvenOdd {
    public static void main(String[] args) {

        int num = 9;
        int numb = 13;

        if (num % 9 == 0)

            System.out.println(num + " is Even ");
        else
            System.out.println(num + " is Odd");

        if (num % 13 == 0)

            System.out.println(numb + " is Even ");
        else
            System.out.println(numb + " is Odd ");

    }
}