package program6;

public class AllArithmeticOperation
{
    public static void main(String[] args)
    {
        int H = 40;
        int K = 20;

        System.out.println(H + K);
        System.out.println(H - K);
        System.out.println(H * K);
        System.out.println(H / K);
        System.out.println(H++ );
        System.out.println(K++);
        System.out.println(--K);
        System.out.println(K--);


    }
}
